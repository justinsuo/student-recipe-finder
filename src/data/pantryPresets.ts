// to actually include every catalog id. The brainstormed seed list only
// had ~30 anchors (because the schema produced JSON for the agent) — the
// preset is meant to load the entire catalog when picked. We splice live
// so the list stays in sync as new ingredients get added.
export const PANTRY_PRESETS: PantryPreset[] = RAW_PANTRY_PRESETS.map((p) => {
  if (p.id !== "everything-pantry") return p;
  const all = INGREDIENTS.map((i) => i.id);
  return { ...p, ingredientIds: Array.from(new Set([...p.ingredientIds, ...all])) };
});

